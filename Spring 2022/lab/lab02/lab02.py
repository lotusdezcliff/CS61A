def multiple(a, b):
    from math import gcd
    # lcm(a, b) = a * b // gcd(a, b)
    """Return the smallest number n that is a multiple of both a and b.

    >>> multiple(3, 4)
    12
    >>> multiple(14, 21)
    42
    """
    "*** YOUR CODE HERE ***"
    return a * b // gcd(a, b)



